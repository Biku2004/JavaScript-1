const money = 5000;

switch(money){
    case 2000:
        console.log("The money is 2000");
        break;

    case 3000:
        console.log("The money is 5000");
        break;

    case 5000:
        console.log("The money is 5000");
        break;

    default:
        console.log("The money is not 2000, 3000 or 5000");
        break;
        
}